local _M = {}

_M.show_version = function(self_main)
   local xml = CScriptXmlInit()
   xml:ParseFile("ui_main_menu.xml")
   version=xml:InitStatic("sigerous_mod_version",self_main)
   version:TextControl():SetText(game.translate_string("st_mod_version_title").. " 2.2.1")
end

return _M
